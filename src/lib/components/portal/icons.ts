/** Get SVG path for a service icon. Falls back to server icon. */
export function getServiceIconPath(icon: ServiceIconId): string {
	return SERVICE_ICON_PATHS[icon] || SERVICE_ICON_PATHS.server;
}

/** Map activity type to icon ID */
export function getActivityIconId(type: string): ServiceIconId {
	if (type === 'compute') return 'server';
	if (type === 'database') return 'database';
	return 'network';
}
